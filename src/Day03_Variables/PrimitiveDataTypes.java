package Day03_Variables;

public class PrimitiveDataTypes {
	
	/*
	 byte: we can assign whole numbers. Must be with in -128 <= byte <=127
	 
	 short: we can assign whole numbers. Must be with in -32768 <= byte <=32767
	 
	 int: we can assign whole numbers. Must be with in -2147483648 <= byte <=2147483647
	 
	 long: we can assign whole numbers. Must be with in -92223372036854775808 <= byte <=92223372036854775807
	 
	 float: we can assign decimal numbers.
	 
	 */

	
	public static void main(String[] args) {
		
		byte length = 3;
		System.out.println(length);
		
		byte width = 4;
		System.out.println(width);
		
		// byte 9num=100; variable name must start with alphabet a-z, A-Z
		
		byte num_and$ = 90; // variable names cannot contain any special characters 
		
		short VariableName = 32000;
		
		int NumInt = 214_748_367;
		
		long NumLong = 10000000l;
		
		
		byte byteNumber = 10;
				
		short shortNumber = byteNumber;
		
		System.out.println(shortNumber);
		
		float floatNumber = 10.5f;
		
		
		
		
	
		
		
	}
}
