package Day03_Variables;

public class EscapeSequences {
	/*
	 \n: new line
	 \t: Horizantal tab
	 \\: Prints single back slash
	 \': Prints single qoute
	 \": Prints double qoute
	 */
	
	
	public static void main(String[] args) {
		
		System.out.println("Hello Cybertek\n Batch 12 students");
		
		System.out.print("Tomorrow is of \n");
		System.out.print("True\n");
		System.out.print("False\n\n\n");
		
		System.out.println("\tClass starts tomorrow");
		
		System.out.println("\\Tugrul");
		System.out.println("\'Tugrul\'");
		System.out.println("\"Tugrul\"");
		
	}

}
